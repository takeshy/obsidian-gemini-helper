import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { gzipSync } from "node:zlib";

const REPO_ROOT = process.cwd();
const SOURCE_ROOT = path.join(REPO_ROOT, "docs", "okf", "gemini-helper-help");
const OUTPUT_FILE = path.join(REPO_ROOT, "src", "generated", "builtinOkfData.ts");
const MAX_BODY_CHARS = 20_000;

function normalizePath(filePath) {
  return filePath.split(path.sep).join("/");
}

function compactBody(body) {
  return body.trim().slice(0, MAX_BODY_CHARS);
}

function parseScalar(value) {
  const trimmed = value.trim();
  if (
    (trimmed.startsWith("\"") && trimmed.endsWith("\"")) ||
    (trimmed.startsWith("'") && trimmed.endsWith("'"))
  ) {
    return trimmed.slice(1, -1);
  }
  return trimmed;
}

function parseFrontmatter(content) {
  const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?([\s\S]*)$/);
  if (!match) return { frontmatter: {}, body: content };

  const frontmatter = {};
  const lines = match[1].split(/\r?\n/);
  for (let i = 0; i < lines.length; i += 1) {
    const line = lines[i];
    const pair = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (!pair) continue;

    const [, key, rawValue] = pair;
    if (rawValue.trim() === "") {
      const values = [];
      while (i + 1 < lines.length) {
        const next = lines[i + 1];
        const item = next.match(/^\s*-\s*(.*)$/);
        if (!item) break;
        values.push(parseScalar(item[1]));
        i += 1;
      }
      frontmatter[key] = values;
      continue;
    }

    const inlineList = rawValue.trim().match(/^\[(.*)\]$/);
    if (inlineList) {
      frontmatter[key] = inlineList[1]
        .split(",")
        .map(item => parseScalar(item))
        .filter(Boolean);
    } else {
      frontmatter[key] = parseScalar(rawValue);
    }
  }

  return { frontmatter, body: match[2] };
}

function asString(value) {
  return typeof value === "string" ? value.trim() : "";
}

function asTags(value) {
  if (Array.isArray(value)) {
    return value.filter(item => typeof item === "string").map(item => item.trim()).filter(Boolean);
  }
  if (typeof value === "string") {
    return value.split(",").map(item => item.trim()).filter(Boolean);
  }
  return [];
}

function isIndexFile(filePath) {
  return filePath.toLowerCase() === "index.md" || filePath.toLowerCase().endsWith("/index.md");
}

async function listMarkdownFiles(dir, base = dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  entries.sort((a, b) => a.name.localeCompare(b.name));

  const files = [];
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...await listMarkdownFiles(fullPath, base));
    } else if (entry.isFile() && entry.name.toLowerCase().endsWith(".md")) {
      const relativePath = normalizePath(path.relative(base, fullPath));
      if (relativePath !== "log.md" && !relativePath.endsWith("/log.md")) {
        files.push({ fullPath, relativePath });
      }
    }
  }
  return files;
}

async function main() {
  const files = await listMarkdownFiles(SOURCE_ROOT);
  const documents = [];

  for (const file of files) {
    const content = await readFile(file.fullPath, "utf8");
    const { frontmatter, body } = parseFrontmatter(content);
    documents.push({
      path: file.relativePath,
      type: asString(frontmatter.type) || (isIndexFile(file.relativePath) ? "Index" : "Concept"),
      title: asString(frontmatter.title) || file.relativePath.replace(/\.md$/i, ""),
      description: asString(frontmatter.description),
      tags: asTags(frontmatter.tags),
      body: compactBody(body),
    });
  }

  const compressedDocuments = gzipSync(Buffer.from(JSON.stringify(documents), "utf8")).toString("base64");
  const output = `// Generated by scripts/generate-builtin-okf.mjs. Do not edit by hand.

export const BUILTIN_OKF_BUNDLE_ID = "__builtin__/gemini-helper-help";
export const BUILTIN_OKF_BUNDLE_NAME = "Gemini Helper Help";
export const BUILTIN_OKF_DOCUMENT_COUNT = ${documents.length};
export const BUILTIN_OKF_DOCUMENTS_GZIP_BASE64 = ${JSON.stringify(compressedDocuments)};
`;

  await mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
  await writeFile(OUTPUT_FILE, output, "utf8");
  console.log(`Generated ${normalizePath(path.relative(REPO_ROOT, OUTPUT_FILE))} from ${documents.length} OKF documents.`);
}

await main();
